#include "3-calc.h"

/**
 * op_add - adds a plus b
 * @a: number
 * @b: another number
 * Return: Always 0 (Success)
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts a minus b
 * @a: number
 * @b: another number
 * Return: Always 0 (Success)
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies a and b
 * @a: number
 * @b: another number
 * Return: Always 0 (Success)
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides a by b
 * @a: number
 * @b: another number
 * Return: Always 0 (Success)
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns remainder of division of a by b
 * @a: number
 * @b: another number
 * Return: Always 0 (Success)
 */

int op_mod(int a, int b)
{
	return (a % b);
}

